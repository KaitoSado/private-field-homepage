import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { writeEnglishDeckArtifacts } from "./lib/english-deck-artifacts.mjs";

const inputPath = resolve("英単語/target1900_normalized.tsv");
const outputPath = resolve("lib/english-target1900.js");
const expectedHeader = ["id", "word", "answerJa", "family", "pos", "unit", "order"];

function parseTsv(text) {
  const [headerLine, ...lines] = text.trim().split(/\r?\n/);
  const header = headerLine.split("\t");

  if (header.join("\t") !== expectedHeader.join("\t")) {
    throw new Error(`Unexpected TSV header: ${headerLine}`);
  }

  return lines.map((line, index) => {
    const columns = line.split("\t");

    if (columns.length !== expectedHeader.length) {
      throw new Error(`Line ${index + 2} has ${columns.length} columns: ${line}`);
    }

    return Object.fromEntries(header.map((key, columnIndex) => [key, columns[columnIndex].trim()]));
  });
}

function posLabel(pos) {
  switch (pos) {
    case "noun":
      return "名詞";
    case "verb":
      return "動詞";
    case "adjective":
      return "形容詞";
    case "adverb":
      return "副詞";
    case "phrase":
      return "熟語";
    default:
      return "その他";
  }
}

function toEnglishEntry(row) {
  const order = Number.parseInt(row.order, 10);

  if (!row.id || !row.word || !row.answerJa || !Number.isFinite(order)) {
    throw new Error(`Invalid vocabulary row: ${JSON.stringify(row)}`);
  }

  return {
    id: row.id,
    headword: row.word,
    coreChunk: row.word,
    meaning: row.answerJa,
    nuance: `${row.word} は「${row.answerJa}」。`,
    grammarNote: `品詞: ${posLabel(row.pos)} / 語族: ${row.family || row.word}`,
    relatedChunks: row.family && row.family !== row.word ? [row.family] : [],
    starterExamples: [
      {
        topic: "all",
        text: row.word,
        cloze: row.word,
        ja: row.answerJa
      }
    ],
    diverseExamples: [],
    shadowPrompts: [row.word],
    family: row.family || row.word,
    pos: row.pos || "other",
    unit: row.unit || "core",
    order
  };
}

const rows = parseTsv(readFileSync(inputPath, "utf8"));
const vocabulary = rows.map(toEnglishEntry);
const { chunkLibrary, outputPath: deckOutputPath, manifestPath } = writeEnglishDeckArtifacts({
  deckId: "basic",
  entries: vocabulary
});
const source = `// Generated by scripts/build-english-vocabulary.mjs from 英単語/target1900_normalized.tsv.
// Do not edit this file by hand; update the TSV and rerun the generator.

export const ENGLISH_TARGET1900_LIBRARY = ${JSON.stringify(vocabulary, null, 2)};
`;

writeFileSync(outputPath, source, "utf8");
console.log(`Generated ${vocabulary.length} English vocabulary entries -> ${outputPath}`);
console.log(`Generated ${chunkLibrary.length} basic English chunks -> ${deckOutputPath}`);
console.log(`Updated deck manifest -> ${manifestPath}`);
